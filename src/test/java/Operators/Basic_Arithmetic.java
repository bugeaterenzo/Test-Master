package Operators;

public class Basic_Arithmetic {

    public static void main(String[] args) {

        int a = 15;
        int b = 4;

        // Perform arithmetic operations
        int addition = a + b;
        int subtraction = a - b;
        int multiplication = a * b;
        int division = a / b;
        int remainder = a % b;

        // Display the results
        System.out.println("Addition: " + addition);        // 15 + 4 = 19
        System.out.println("Subtraction: " + subtraction);  // 15 - 4 = 11
        System.out.println("Multiplication: " + multiplication); // 15 * 4 = 60
        System.out.println("Division: " + division);        // 15 / 4 = 3
        System.out.println("Remainder: " + remainder);      // 15 % 4 = 3

    }

}
