package Variables;

public class Multiple_Assignments {

    public static void main(String[] args) {


//      In Java, you can assign multiple variables in a single line as
//      long as they are of the same type or separated by a comma while
//      maintaining the correct data types.

        double coffeePrice = 5.50;
        boolean isCappuccinoAvailable = true;
        double shopRating = 4.8;

        // Print the variables
        System.out.println("Coffee Price: $" + coffeePrice);
        System.out.println("Is Cappuccino Available: " + isCappuccinoAvailable);
        System.out.println("Shop Rating: " + shopRating);


    }

}
