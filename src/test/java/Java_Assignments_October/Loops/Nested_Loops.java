package Java_Assignments_October.Loops;

public class Nested_Loops {


    public static void main(String[] args) {


        for ( int i = 1; i <=5; i++)
        {
            for ( int x = 1; x <= (2 * i - 1); x++)
            {
                System.out.print(x + " ");
            }
            System.out.println();
        }








    }



}
