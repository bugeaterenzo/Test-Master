package Java_Assignments_October.Methods;

import java.lang.reflect.Array;
import java.util.Arrays;
import java.util.Map;

public class Print_Entry
{
    public void print ( String entry )
    {
        System.out.println(entry);
    }

    public void print ( int entry )
    {
        System.out.println(entry);
    }

    public void print ( double entry )
    {
        System.out.println(entry);
    }

    public void print ( boolean entry )
    {
        System.out.println(entry);
    }

    public void print ( Map entry )
    {
        System.out.println(entry);
    }

    public void print ( String[] entry )
    {
        System.out.println(Arrays.toString(entry));
    }

    public void print ( int[] entry )
    {
        System.out.println(Arrays.toString(entry));
    }

    public void print ( double[] entry )
    {
        System.out.println(Arrays.toString(entry));
    }

    public void print ( Integer[] entry )
    {
        System.out.println(Arrays.toString(entry));
    }
}
