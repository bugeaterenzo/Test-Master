package Java_References.Set;

public class Hashset
{
    public static void main(String[] args)
    {

        // HashSet

        // Implemented Interfaces:

        // Set<E>: HashSet implements the Set interface, which means it follows the
        // general behavior expected of a set (e.g., no duplicate elements).
        // Cloneable: This allows objects of HashSet to be cloned.
        // Serializable: This allows the HashSet to be serialized.
        // Iterable<E>: This allows a HashSet to be used in enhanced for-loops and iterators.

        // Characteristics:

        // HashSet does not maintain any order of elements.
        // The set is backed by a hash table, so the elements are organized by their hash codes.
        // Does not implement NavigableSet or SortedSet, so it does not have the sorting
        // or navigation methods like ceiling(), floor(), etc.
    }
}
