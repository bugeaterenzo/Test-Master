package Java_References.Set;

public class Linked_Hashset
{
    public static void main(String[] args)
    {


        // LinkedHashSet

        // Implemented Interfaces:

        // Set<E>: Like HashSet, LinkedHashSet implements Set.
        // Cloneable: This allows objects of LinkedHashSet to be cloned.
        // Serializable: This allows the LinkedHashSet to be serialized.
        // Iterable<E>: This allows LinkedHashSet to be used in enhanced for-loops and iterators.


        // Characteristics:

        // LinkedHashSet maintains insertion order of elements (the order in which elements were added).
        // It is implemented using a hash table with a linked list to preserve order.
        // Does not implement NavigableSet or SortedSet, so it lacks the navigation methods like
        // ceiling(), floor(), etc., found in TreeSet.

    }
}
