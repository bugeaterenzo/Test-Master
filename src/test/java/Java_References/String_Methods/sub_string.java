package Java_References.String_Methods;

public class sub_string
{
    public static void main(String[] args)
    {


        String myStr = "Hello, World!";

        System.out.println( myStr.substring(7, 12) );

        // substring():

        // Defined in the String class.
        // Operates specifically on String instances.
        // Not a method of the CharSequence interface,
        // so it doesn't apply to other types like StringBuilder or StringBuffer.




    }


}
