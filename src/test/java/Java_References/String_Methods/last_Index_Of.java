package Java_References.String_Methods;

public class last_Index_Of
{
    public static void main(String[] args)
    {

        // The lastIndexOf() method returns the position of the last
        // occurrence of specified character(s) in a string.

        String myStr = "Hello planet earth, you are a great planet.";

        System.out.println(myStr.lastIndexOf("planet"));



    }
}
